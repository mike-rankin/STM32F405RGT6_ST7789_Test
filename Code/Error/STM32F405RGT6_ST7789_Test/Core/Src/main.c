static void UART_Print(const char *str)
{
    HAL_UART_Transmit(&huart1, (uint8_t *)str, (uint16_t)strlen(str), 100);
}


void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    CST816S_EXTI_Callback(&touch, GPIO_Pin);
}


static void my_touchpad_read(lv_indev_drv_t *indev_drv, lv_indev_data_t *data)
{
    if (CST816S_Available(&touch))
    {
     data->state   = LV_INDEV_STATE_PRESSED;
     data->point.x = (lv_coord_t)((280 /*DISPLAY_WIDTH*/ - 1) - touch.touch.y);
     data->point.y = (lv_coord_t)touch.touch.x;
     char buf[56];
        snprintf(buf, sizeof(buf), "Touch: x=%u  y=%u  gesture=0x%02X\r\n",
                 touch.touch.x, touch.touch.y, (uint8_t)touch.touch.gesture);
        UART_Print(buf);
    }
    else
    {
     data->state = LV_INDEV_STATE_RELEASED;
    }
}


void Fade_Led(void)
{
 int x;
 for(x=0; x<1000; x=x+1)
 {
  __HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_1, x);
  HAL_Delay(1);
 }
 for(x=1000; x>0; x=x-1)
 {
  __HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_1, x);
  HAL_Delay(1);
 }
}





int main(void)
{

  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_DMA_Init();
  MX_CAN2_Init();
  MX_I2C1_Init();
  MX_SPI1_Init();
  MX_USART1_UART_Init();
  MX_TIM1_Init();

  bool ok = CST816S_Init(&touch, &hi2c1, C_RST_GPIO_Port, C_RST_Pin,C_INT_GPIO_Port, C_INT_Pin);

  if (!ok)
  {
   UART_Print("CST816S not found on I2C bus!\r\n");
   Error_Handler();
  }

  HAL_GPIO_WritePin(GPIOA, GPIO_PIN_1, GPIO_PIN_SET);  // Backlight on
  UART_Print("CST816S initialised OK\r\n");

  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_SET);

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);

  lv_init();
  lv_port_disp_init();

  lv_indev_drv_init(&indev_drv);
  indev_drv.type    = LV_INDEV_TYPE_POINTER;
  indev_drv.read_cb = my_touchpad_read;
  lv_indev_drv_register(&indev_drv);

  ui_init();

  while (1)
  {
   Fade_Led();
   lv_timer_handler();
   HAL_Delay(5);
  }
}

